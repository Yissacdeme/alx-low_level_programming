#include "main.h"
#include <stdio.h>
/**
 * print_to_98 - Prints all natural numbersfrom input to 98
 * inorder separated by a comma followed by a space
 * @n: The number to begin counting at.
 */
void print_to_98(int n)
{
	if (n < 98)
	{
		while (n <= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n++;
		}
	}
	else if (n > 98)
	{
		while (n >= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n--;
		}
	}
	else
	{
		printf("98");
	}
	printf("\n");

}
